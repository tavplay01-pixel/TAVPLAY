// Função para obter versículo do dia
export function getDailyVerse(): Verse {
  const today = new Date();
  const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / 86400000);
  return DAILY_VERSES[dayOfYear % DAILY_VERSES.length];
}

// Função para obter versículo de encorajamento aleatório
export function getRandomEncouragementVerse(): Verse {
  return ENCOURAGEMENT_VERSES[Math.floor(Math.random() * ENCOURAGEMENT_VERSES.length)];
}

// Função para obter versículo de conclusão de jogo
export function getGameCompletionVerse(gameId: string): Verse {
  return GAME_COMPLETION_VERSES[gameId] || ENCOURAGEMENT_VERSES[0];
}
